# convert input to float
n1 = float(input('Enter the first number:\n'))
n2 = float(input('Enter the second number:\n'))

# print results
print(f'{n1} + {n2} = {n1 + n2}')
print(f'{n1} - {n2} = {n1 - n2}')
print(f'{n1} * {n2} = {n1 * n2}')
print(f'{n1} / {n2} = {n1 / n2}')
print(f'{n1} // {n2} = {n1 // n2}')
print(f'{n1} % {n2} = {n1 % n2}')
print(f'{n1} ** {n2} = {n1 ** n2}')